    private void RefreshProductsTable() {
        ProductTableModel model = new ProductTableModel();
        
        tabelProduse.setModel(model);
        
        var produse = Produse.GetAllProduse();
        
        for(int i = 0; i < produse.size(); i++) {
            model.add(produse.get(i));
        }
    }
    

        
    
